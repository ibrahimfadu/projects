package collage_programes;

public class Datatypes {

	public static void main(String[] args) {
		byte b=124;
		System.out.println("Byte data type: "+b);
		String S="Java programming language";
		System.out.println("String data type: "+S);
		char c='A';
		System.out.println("Character data type: "+c);
		float f=19.8f;
		System.out.println("Float data type: "+f);
		int i=67;
		System.out.println("Integer data type: "+i);

	}

}
